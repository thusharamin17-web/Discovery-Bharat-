// =======================
// STATE
// =======================
let slideIndex = 0;
let currentImages = [];
let slideTimer = null;

// =======================
// OPEN MENU SECTION
// =======================
function openPage(section) {
    showOverlay(data[section]);
}

// =======================
// OVERLAY
// =======================
function showOverlay(info) {
    const overlay = document.getElementById("overlay");
    document.getElementById("title").innerText = info.title;
    document.getElementById("content").innerHTML = info.text;

    currentImages = info.images;
    slideIndex = 0;

    buildDots(currentImages.length);
    showSlides();

    overlay.classList.add('active');
    overlay.style.display = 'flex';
}

function closePage() {
    document.getElementById("overlay").classList.remove('active');
    document.getElementById("overlay").style.display = 'none';
    if (slideTimer) clearTimeout(slideTimer);
}

// Close when clicking backdrop
function handleOverlayClick(e) {
    if (e.target === document.getElementById("overlay")) {
        closePage();
    }
}

// Close with Escape key
document.addEventListener('keydown', (e) => {
    if (e.key === 'Escape') closePage();
});

// =======================
// SLIDESHOW
// =======================
function showSlides() {
    if (currentImages.length === 0) return;
    if (slideTimer) clearTimeout(slideTimer);

    const img = document.getElementById("slideImg");

    // Fade transition
    img.style.opacity = '0';
    setTimeout(() => {
        img.src = currentImages[slideIndex];
        img.style.opacity = '1';
        img.style.transition = 'opacity 0.6s ease';
    }, 200);

    updateDots(slideIndex);

    slideIndex = (slideIndex + 1) % currentImages.length;
    slideTimer = setTimeout(showSlides, 2800);
}

// =======================
// SLIDE DOTS
// =======================
function buildDots(count) {
    const container = document.getElementById("slideDots");
    container.innerHTML = '';
    for (let i = 0; i < count; i++) {
        const dot = document.createElement('div');
        dot.className = 'slide-dot' + (i === 0 ? ' active' : '');
        dot.onclick = () => {
            slideIndex = i;
            if (slideTimer) clearTimeout(slideTimer);
            showSlides();
        };
        container.appendChild(dot);
    }
}

function updateDots(index) {
    const dots = document.querySelectorAll('.slide-dot');
    dots.forEach((d, i) => {
        d.classList.toggle('active', i === index);
    });
}
